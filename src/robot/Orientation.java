package robot;

/**
 * A enum class representing the direction the robot is facing.
 */
public enum Orientation {

    /**
     * Used to know when the robot are facing north.
     */
    NORTH,
    /**
     * Used to know when the robot are facing south.
     */
    SOUTH,
    /**
     * Used to know when the robot are facing east.
     */
    EAST,
    /**
     * Used to know when the robot are facing west.
     */
    WEST

}
