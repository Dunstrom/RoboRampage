package entity;

/**
 * A enum class representing the direction the entity.robot is facing.
 */
public enum Orientation {

    /**
     * Used to know when the entity.robot are facing north.
     */
    NORTH,
    /**
     * Used to know when the entity.robot are facing south.
     */
    SOUTH,
    /**
     * Used to know when the entity.robot are facing east.
     */
    EAST,
    /**
     * Used to know when the entity.robot are facing west.
     */
    WEST

}
